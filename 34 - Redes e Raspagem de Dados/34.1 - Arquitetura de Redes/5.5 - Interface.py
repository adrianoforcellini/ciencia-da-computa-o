# Interface / Acesso ao Meio

# Por último, mas não menos importante, temos a camada física ou de 
# abstração do hardware, também chamada de camada de interface ou de 
# acesso ao meio.

# A principal função dessa camada é realizar a interface do modelo 
# TCP/IP com os diversos modelos de rede, por exemplo o protocolo Ethernet ,
# transmitindo os dados através dos meios físicos, encontrando e transmitindo
# tudo pelo melhor caminho possível. Esta camada lida com os meios de 
# comunicação, corresponde ao nível de hardware, ou meio físico, 
# que trata dos sinais eletrônicos, conector, pinagem, níveis de tensão,
#  dimensões físicas, características mecânicas e elétricas, etc.
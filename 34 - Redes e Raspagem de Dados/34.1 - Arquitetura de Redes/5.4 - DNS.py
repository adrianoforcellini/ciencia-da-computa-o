# DNS

# Estamos falando constantemente de endereços IP: 
# que toda máquina possui um para poder se comunicar na rede e ao endereçar
# um pacote nós o utilizamos. Porém, a realidade é que não costumamos ver 
# muitos esses números ao utilizar a internet e você deve estar se perguntando
# que, se eles são essenciais para se navegar, onde eles ficam?

# Nós utilizamos um sistema de nomes para identificar pontos da rede, 
# ao invés de usar números, já que nomes são mais fáceis de serem utilizados 
# por pessoas. Por exemplo: imagine que, para acessar o google.com fosse 
# necessário digitar no navegador "8.8.8.8", ou para acessar o site da Trybe 
# fosse necessário digitar "34.193.204.92". Seria muito complexo e nada fácil,
# certo? Para simplificar isso existe o sistema de DNS .

# Dessa forma, de maneira bem resumida, conseguimos atribuir um "nome" 
# a um endereço IP específico.

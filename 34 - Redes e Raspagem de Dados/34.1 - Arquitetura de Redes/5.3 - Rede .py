# Rede

# O principal protocolo utilizado nessa camada é o IP , que inclusive dá nome
# ao modelo. Outras opções de protocolos dessa camada temos o ICMP,
# NAT, ARP. Todos eles lidam com o endereçamento da comunicação.
# Mas o que seria o endereçamento?!

# Imagine que você vai enviar uma mensagem para alguém através de uma carta.
# Você então escreve a mensagem em uma folha e a coloca em um envelope. 
# Nesse envelope é necessário que você coloque o endereço para o destinatário
# a qual você está enviando a mensagem, de modo que seja possível entregá-la.
# Da mesma maneira você precisa informar o seu endereço para que o 
# destinatário possa enviar uma mensagem de resposta para você.
# A camada de rede do TCP/IP é utilizada para isso.
# Ela irá identificar o remetente e o destinatário para que o pacote possa
# ser transmitido na rede. Caso queira conhecer um pouco mais sobre o 
# IPv6 assista, como conteúdo extra, ao vídeo: 'Os endereços IP não são
# todos iguais do NIC.br'
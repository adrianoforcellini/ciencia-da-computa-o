# Proxy
# Outro componente que pode fornecer mais uma camada de segurança quando falamos 
# de redes é o proxy. O proxy provém uma camada a mais de controle entre a 
# internet e os dispositivos da rede, e pode ser utilizado para diversos fins.
# Um uso comum dos proxies é como uma espécie de filtro que, através de 
# características de uma conexão, ou tráfego, consegue determinar se esse tr
# táfego deve ser feito ou não. Por exemplo, para evitar acessos a redes 
# sociais em escritórios o proxy pode ser utilizado para reconhecer palavras 
# chaves em URLs e então recusá-la. Além disso, eles podem impedir que pessoas 
# usuárias acessem sites com conteúdos impróprios ou com potencial de ser uma 
# página maliciosa.
